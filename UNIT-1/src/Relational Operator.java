class Relational{
    public static void main(String[] args) {
        System.out.println(5>2);
        System.out.println(4>7);
        System.out.println(4<5);
        System.out.println(8<4);
        System.out.println(7==7);
        System.out.println(8==7);
        System.out.println(7!=8);
    }
}