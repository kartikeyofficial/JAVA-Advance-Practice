import java.util.Scanner;
public class ShorthandOperator {
    public static void main(String[] args) {
        int a=5;
        Scanner x= new Scanner(System.in);
        int x1= x.nextInt();
        a +=x1;
        System.out.println(a);
        int x2 = x.nextInt();
        a +=x2;
        System.out.println(a);
        int x3= x.nextInt();
        a +=x3;
        System.out.println(a);
        int x4= x.nextInt();
        a +=x4;
        System.out.println(a);

    }
}
